static inline bool aux_sym_expr_binop_token2_character_set_1(int32_t c) {
  return (c < '?'
    ? (c < '*'
      ? (c < '$'
        ? c == '!'
        : c <= '\'')
      : (c <= '+' || (c < ':'
        ? (c >= '-' && c <= '/')
        : c <= ':')))
    : (c <= '@' || (c < '|'
      ? (c < '^'
        ? c == '\\'
        : c <= '^')
      : (c <= '|' || c == '~'))));
}

static inline bool aux_sym_expr_binop_token3_character_set_1(int32_t c) {
  return (c < '='
    ? (c < '*'
      ? (c < '$'
        ? c == '!'
        : c <= '\'')
      : (c <= '+' || (c < ':'
        ? (c >= '-' && c <= '/')
        : c <= ':')))
    : (c <= '=' || (c < '^'
      ? (c < '\\'
        ? (c >= '?' && c <= '@')
        : c <= '\\')
      : (c <= '^' || (c < '~'
        ? c == '|'
        : c <= '~')))));
}

